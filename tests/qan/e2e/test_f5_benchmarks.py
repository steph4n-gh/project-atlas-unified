def test_t2_benchmarks_vram_limit():
    """
    Verify strict enforcement of VRAM limits under linear projection up to 500k context.
    """
    # Read qa_report.json generated by run_validation_suite.py
    report_path = "results/qa_report.json"
    if not os.path.exists(report_path):
        pytest.fail("Run validation suite plot generation test first to create qa_report.json")
        
    with open(report_path, "r") as f:
        report = json.load(f)
        
    proj_dense = report["memory_scaling"]["projected_500k_dense_vram_gb"]
    proj_qan = report["memory_scaling"]["projected_500k_qan_vram_gb"]
    ceiling = report["memory_scaling"]["vram_ceiling_gb"]
    
    assert proj_qan < 15.0, f"QAN-ATLAS projected memory exceeds 15 GB ceiling: {proj_qan:.2f} GB"
    assert proj_dense > ceiling, f"Dense attention should exceed VRAM ceiling of {ceiling} GB, got {proj_dense:.2f} GB"

