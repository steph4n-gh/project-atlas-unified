def test_t1_benchmarks_context_scaling():
    """
    Verify KV-cache memory savings of QuasicrystallineAttention vs DenseAttention.
    """
    embed_dim = 64
    num_heads = 4
    sparse_ratio = 0.15
    
    dense_layer = DenseAttention(embed_dim=embed_dim, num_heads=num_heads, sparse_ratio=sparse_ratio)
    qan_layer = QuasicrystallineAttention(embed_dim=embed_dim, num_heads=num_heads, sparse_ratio=sparse_ratio)
    
    dense_layer.eval()
    qan_layer.eval()
    
    S = 1000
    x = torch.randn(1, S, embed_dim)
    
    cache_dense = {}
    cache_qan = {}
    
    with torch.no_grad():
        _ = dense_layer(x, kv_cache=cache_dense)
        _ = qan_layer(x, kv_cache=cache_qan)
        
    dense_size = cache_dense["K"].nelement() + cache_dense["V"].nelement()
    qan_size = cache_qan["K"].nelement() + cache_qan["V"].nelement()
    
    # QAN should save at least 80% VRAM (since sparse_ratio is 0.15)
    saving = 1.0 - (qan_size / dense_size)
    assert saving >= 0.8, f"Expected VRAM saving >= 80%, got {saving:.2%}"

def test_t1_benchmarks_convergence_proof():
    """
    Verify AdelicLangevinOptimizer converges (loss decreases) on a quadratic objective.
    """
    p = torch.nn.Parameter(torch.tensor([1.5, -1.0, 2.0]))
    opt = AdelicLangevinOptimizer([p], lr=0.05, alpha=0.75, T_0=0.01)
    
    target = torch.tensor([0.0, 0.0, 0.0])
    
    initial_loss = float((0.5 * torch.sum((p - target)**2)).item())
    
    for _ in range(30):
        loss = 0.5 * torch.sum((p - target)**2) + torch.sum(p * torch.randn(3) * 0.05)
        opt.zero_grad()
        loss.backward()
        opt.step()
        
    final_loss = float((0.5 * torch.sum((p - target)**2)).item())
    assert final_loss < initial_loss, f"Optimizer failed to converge: final loss {final_loss} >= initial {initial_loss}"

def test_t1_benchmarks_long_document_qa():
    """
    Verify needle-in-haystack QA retrieval in AdelicMemorySwapGridDB.
    """
    d_model = 32
    db = AdelicMemorySwapGridDB(d_model=d_model, device="cpu", cache_limit_ratio=0.15)
    
    corpus_keys = torch.randn(1000, d_model)
    corpus_vals = torch.randn(1000, d_model)
    
    # Unique needle key
    needle_key = corpus_keys[500:501].clone()
    needle_key[0, 0] = 5.0
    corpus_keys[500] = needle_key[0]
    
    db.swap_out(corpus_keys, corpus_vals)
    
    retrieved_keys, retrieved_vals = db.swap_in(needle_key)
    
    found = False
    if retrieved_keys.shape[0] > 0:
        dists = torch.norm(retrieved_keys - needle_key, dim=-1)
        if (dists < 1e-4).any():
            found = True
            
    assert found, "Needle key was not retrieved from E8 Grid DB"

